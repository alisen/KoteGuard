# Markdown helpers
# ---------------------------------------------------------------------------


def _h2(title: str, items: list[str]) -> str:
    lines = [f"## {title}", ""]
    for item in items:
        lines.append(f"- {item}")
    lines.append("")
    return "\n".join(lines)


def _h2_text(title: str, body: str) -> str:
    return f"## {title}\n\n{body}\n\n"


