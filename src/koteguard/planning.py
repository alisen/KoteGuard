def render_task(task: TaskModel) -> str:
    """Serialize a TaskModel to a TASK.md string with YAML front-matter."""
    data = task.model_dump(mode="json")
    front_matter = yaml.dump(data, default_flow_style=False, sort_keys=False, allow_unicode=True)

    constraints_md = "\n".join(f"- {c}" for c in task.constraints) or "- (none)"

    body = f"""\
# Task: {task.description}

**Session:** `{task.session_id}`

## Context

{task.context or "(none)"}

## Constraints

{constraints_md}
"""
    return f"---\n{front_matter}---\n\n{body}"


def parse_task(markdown: str) -> TaskModel:
    """Parse a TASK.md string back into a TaskModel.

    Prefers YAML front-matter; falls back to regex on error.
    """
    if markdown.startswith("---"):
        try:
            parts = markdown.split("---", 2)
            if len(parts) >= 3:
                data = yaml.safe_load(parts[1])
                if isinstance(data, dict) and "session_id" in data:
                    return TaskModel.model_validate(data)
        except (yaml.YAMLError, Exception):
            pass

    # Minimal regex fallback
    session_m = re.search(r"`([a-z0-9][a-z0-9\-]{2,})`", markdown)
    session_id = session_m.group(1) if session_m else "unknown-session"
    desc_m = re.search(r"^#\s+Task:\s+(.+)$", markdown, re.MULTILINE)
    description = desc_m.group(1).strip() if desc_m else "Unnamed task"
    return TaskModel(session_id=session_id, description=description)


