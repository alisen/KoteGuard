def render_plan(plan: PlanModel) -> str:
    """Render a PlanModel to a PLAN.md string with YAML front-matter spec block."""
    # Serialize model to plain dict for YAML (PlanTask objects become dicts)
    data = plan.model_dump(mode="json")
    front_matter = yaml.dump(data, default_flow_style=False, sort_keys=False, allow_unicode=True)

    # Human-readable markdown body
    tasks_md_lines = [
        f"{i}. [{('x' if t.done else ' ')}] {t.description}" for i, t in enumerate(plan.tasks, 1)
    ]
    tasks_md = "\n".join(tasks_md_lines)

    body_lines: list[str] = [
        f"# {plan.title}",
        "",
        f"> Generated by KoteGuard on {datetime.now(tz=UTC).strftime('%Y-%m-%d %H:%M UTC')}",
        "> The YAML block above is the machine-readable spec — edit task `done` fields there.",
        "",
        _h2("Objectives", plan.objectives),
        "## Tasks\n",
        tasks_md,
        "",
        _h2("Definition of Done", plan.definition_of_done),
        f"## Estimated Time\n\n{plan.estimated_time}\n",
    ]
    if plan.risks:
        body_lines.append(_h2("Risks", plan.risks))
    if plan.android_skills:
        body_lines.append(_h2("Android Skills", plan.android_skills))

    body_lines.append(
        "## Token & Context Rules (enforced by KoteGuard)\n\n"
        "- Batch all subtasks into ONE TASK.md (do not send multiple messages)\n"
        "- Use EDIT on this PLAN.md instead of follow-up messages\n"
        "- Choose model explicitly: fast/haiku for drafts, sonnet/full for real work\n"
        "- Prefer short, focused sessions — Copilot CLI auto-compacts at ~80% context window\n"
        "- After session: summarize key decisions back to WORKSPACE.md via kote cleanup --compact\n"
    )

    body = "\n".join(body_lines)
    return f"---\n{front_matter}---\n\n{body}"


def parse_plan(markdown: str) -> PlanModel:
    """Parse a PLAN.md string back into a PlanModel.

    Strategy:
    1. If YAML front-matter present → yaml.safe_load → PlanModel.model_validate
    2. yaml.YAMLError or no front-matter → legacy regex fallback
    """
    # --- Layer 1: YAML front-matter ---
    if markdown.startswith("---"):
        try:
            parts = markdown.split("---", 2)
            if len(parts) >= 3:
                fm_str = parts[1]
                data = yaml.safe_load(fm_str)
                if isinstance(data, dict) and "title" in data:
                    return PlanModel.model_validate(data)
        except (yaml.YAMLError, Exception):
            pass  # fall through to regex

    # --- Layer 2: Legacy regex fallback ---
    return _parse_plan_regex(markdown)


def _parse_plan_regex(markdown: str) -> PlanModel:
    """Legacy regex-based PLAN.md parser for files without YAML front-matter."""

    def _extract_h1(text: str) -> str:
        m = re.search(r"^#\s+(.+)$", text, re.MULTILINE)
        return m.group(1).strip() if m else "Untitled Plan"

    def _extract_section_list(text: str, section: str) -> list[str]:
        pattern = rf"##\s+{re.escape(section)}\s*\n(.*?)(?=^##|\Z)"
        m = re.search(pattern, text, re.MULTILINE | re.DOTALL)
        if not m:
            return []
        block = m.group(1)
        items: list[str] = []
        for line in block.splitlines():
            line = line.strip()
            m2 = re.match(r"^[-*•]\s+(.+)$", line)
            if m2:
                items.append(m2.group(1))
                continue
            m2 = re.match(r"^\d+\.\s+\[.\]\s+(.+)$", line)  # checkbox format
            if m2:
                items.append(m2.group(1))
                continue
            m2 = re.match(r"^\d+\.\s+(.+)$", line)
            if m2:
                items.append(m2.group(1))
        return items

    def _extract_section_text(text: str, section: str) -> str:
        pattern = rf"##\s+{re.escape(section)}\s*\n(.*?)(?=^##|\Z)"
        m = re.search(pattern, text, re.MULTILINE | re.DOTALL)
        if not m:
            return ""
        return m.group(1).strip()

    raw_tasks = _extract_section_list(markdown, "Tasks") or ["(none)"]
    # raw_tasks are strings — PlanModel validator will coerce to PlanTask
    return PlanModel(
        title=_extract_h1(markdown),
        objectives=_extract_section_list(markdown, "Objectives") or ["(none)"],
        tasks=raw_tasks,
        definition_of_done=_extract_section_list(markdown, "Definition of Done") or ["(none)"],
        estimated_time=_extract_section_text(markdown, "Estimated Time") or "unknown",
        risks=_extract_section_list(markdown, "Risks"),
        android_skills=_extract_section_list(markdown, "Android Skills"),
    )


