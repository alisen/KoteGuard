def render_copilot_instructions(
    plan: PlanModel,
    workspace: WorkspaceModel,
    session_id: str,
    android_cli_enabled: bool = True,
) -> str:
    """Generate .github/copilot-instructions.md content."""
    # Build task list with IDs so agent knows which id to reference
    tasks_md_lines = [
        f"{i}. `{t.id}`: {t.description}{' ✓' if t.done else ''}"
        for i, t in enumerate(plan.tasks, 1)
    ]
    tasks_md = "\n".join(tasks_md_lines)
    dod_md = "\n".join(f"- {d}" for d in plan.definition_of_done)

    # Android section — only shown when CLI is enabled
    android_section = ""
    if workspace.android_agent_stack and android_cli_enabled:
        stack = workspace.android_agent_stack
        skills = stack.get("enabled_skills", [])
        android_section = f"""
## Android Agent Stack

- **CLI Available:** {stack.get("cli_version", "unknown")}
- **Knowledge Base:** {stack.get("kb_status", "unknown")}

### Best Practices

- Always use `android` CLI commands for emulator and device management within the worktree
- Reference enabled skills when implementing features: {", ".join(skills) if skills else "none detected"}
- Prefer Jetpack Compose patterns where applicable
- Use Android-specific coroutine dispatchers (Dispatchers.IO for I/O, Dispatchers.Main for UI)
"""

    return f"""\
# Copilot Agent Instructions (KoteGuard Session: {session_id})

> **This is an isolated git worktree. You are NOT in the primary repository.**
> Changes here are safe to experiment with.

## Project: {workspace.project_name}

**Tech Stack:** {", ".join(workspace.tech_stack)}

## Your Mission: {plan.title}

### Tasks

{tasks_md}

### Definition of Done

{dod_md}

## Spec Updates (IMPORTANT)

When you complete a task, open `PLAN.md` and find the task in the `---` YAML block at the top.
Set `done: true` for that task entry. **Do not reformat any other part of the YAML block.**
Example: find `- id: t1` and change `done: false` to `done: true`.

## Constraints

- Only modify files relevant to the tasks above.
- Do NOT run `git push` or create pull requests without human review.
- Do NOT access the internet except to install declared dependencies.
- Keep commits small and well-described.

## Token & Context Discipline

- Batch all sub-tasks into a single response where possible
- Use fast/haiku models for drafts, full models for real implementation
- Sessions auto-compact at ~80% context window
{android_section}
## Estimated Time

{plan.estimated_time}
"""


def render_security_instructions(
    project_type: str = "unknown",
    android_cli_enabled: bool = True,
) -> str:
    """Generate .github/instructions/security.instructions.md content."""
    android_section = ""
    ios_section = ""

    if project_type in ("android", "monorepo", "unknown"):
        android_base = """
### Android

- NEVER read, copy, or output contents of: `*.jks`, `*.keystore`, `google-services.json`
- `local.properties` must never be committed
- Do not add signing config with hardcoded passwords
"""
        android_cli_block = """
### Android CLI Commands

**Allowed:** `android list avd`, `android list targets`, `android list sdk`
**Forbidden (outside worktree):** `android run`, `android emulator` – only valid inside a kote worktree
"""
        android_section = android_base + (android_cli_block if android_cli_enabled else "")

    if project_type in ("ios", "monorepo", "unknown"):
        ios_section = """
### iOS

- NEVER read, copy, or output contents of: `*.p12`, `*.mobileprovision`, `GoogleService-Info.plist`
- Do not hardcode API keys or bundle IDs in source files
"""

    return f"""\
---
applyTo: "**/*"
---

# Security Instructions (KoteGuard)

> These instructions apply to ALL files in this worktree.

## Deny-by-Default Rules

1. **No secrets in source code** – never hardcode API keys, tokens, passwords.
2. **No git push** – humans review all changes before they leave this worktree.
3. **No branch creation outside kote/** – stay on the assigned branch.
4. **No file exfiltration** – do not print or upload file contents to external services.
{android_section}{ios_section}
## Allowed Git Commands

- `git status`, `git diff`, `git log` – read-only inspection
- `git add`, `git commit` – staging and committing changes
- `git stash` – temporary stash

## Forbidden Git Commands

- `git push` – forbidden without human review
- `git remote add` – forbidden
- `git remote set-url` – forbidden
- `git clone` – forbidden inside worktree
"""
