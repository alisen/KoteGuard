def render_plan(plan: PlanModel) -> str:
    """Render a PlanModel to a PLAN.md string."""
    lines: list[str] = [
        f"# {plan.title}",
        "",
        f"> Generated by KoteGuard on {datetime.now(tz=timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}",
        "",
    ]

    lines.append(_h2("Objectives", plan.objectives))
    lines.append("## Tasks\n")
    for i, task in enumerate(plan.tasks, 1):
        lines.append(f"{i}. {task}")
    lines.append("")
    lines.append(_h2("Definition of Done", plan.definition_of_done))
    lines.append(f"## Estimated Time\n\n{plan.estimated_time}\n")
    if plan.risks:
        lines.append(_h2("Risks", plan.risks))

    return "\n".join(lines)


def parse_plan(markdown: str) -> PlanModel:
    """Parse a PLAN.md markdown string back into a PlanModel."""

    def _extract_h1(text: str) -> str:
        m = re.search(r"^#\s+(.+)$", text, re.MULTILINE)
        return m.group(1).strip() if m else "Untitled Plan"

    def _extract_section_list(text: str, section: str) -> list[str]:
        pattern = rf"##\s+{re.escape(section)}\s*\n(.*?)(?=^##|\Z)"
        m = re.search(pattern, text, re.MULTILINE | re.DOTALL)
        if not m:
            return []
        block = m.group(1)
        items: list[str] = []
        for line in block.splitlines():
            line = line.strip()
            # Bullet or numbered list
            m2 = re.match(r"^[-*•]\s+(.+)$", line)
            if m2:
                items.append(m2.group(1))
                continue
            m2 = re.match(r"^\d+\.\s+(.+)$", line)
            if m2:
                items.append(m2.group(1))
        return items

    def _extract_section_text(text: str, section: str) -> str:
        pattern = rf"##\s+{re.escape(section)}\s*\n(.*?)(?=^##|\Z)"
        m = re.search(pattern, text, re.MULTILINE | re.DOTALL)
        if not m:
            return ""
        return m.group(1).strip()

    return PlanModel(
        title=_extract_h1(markdown),
        objectives=_extract_section_list(markdown, "Objectives") or ["(none)"],
        tasks=_extract_section_list(markdown, "Tasks") or ["(none)"],
        definition_of_done=_extract_section_list(markdown, "Definition of Done")
        or ["(none)"],
        estimated_time=_extract_section_text(markdown, "Estimated Time") or "unknown",
        risks=_extract_section_list(markdown, "Risks"),
    )


