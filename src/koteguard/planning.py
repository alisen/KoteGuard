def render_workspace(ws: WorkspaceModel) -> str:
    """Render a WorkspaceModel to a WORKSPACE.md string."""
    lines: list[str] = [
        f"# WORKSPACE: {ws.project_name}",
        "",
        "> Auto-generated by KoteGuard Phase 0 analysis.",
        "",
    ]

    lines.append(_h2("Tech Stack", ws.tech_stack))

    if ws.architecture:
        lines.append(_h2_text("Architecture", ws.architecture))

    if ws.conventions:
        lines.append(_h2("Conventions", ws.conventions))

    if ws.structure:
        lines.append("## Project Structure\n")
        for path, description in ws.structure.items():
            lines.append(f"- `{path}` – {description}")
        lines.append("")

    if ws.gotchas:
        lines.append(_h2("Gotchas / Watch-outs", ws.gotchas))

    return "\n".join(lines)


def workspace_from_project_info(info: ProjectInfo) -> WorkspaceModel:
    """Build a WorkspaceModel from a ProjectInfo (Phase 0 output)."""
    tech_stack: list[str] = info.frameworks.copy()
    if info.languages:
        tech_stack.extend(info.languages)
    if not tech_stack:
        tech_stack = [info.project_type.value]

    architecture = ""
    gotchas: list[str] = []
    structure: dict[str, str] = {}
    conventions: list[str] = []

    pt = info.project_type.value
    if pt == "android":
        architecture = (
            "Android application using Gradle build system. "
            "Standard Android project structure with app module."
        )
        if info.android_package:
            structure["app/src/main/"] = (
                f"Main source code (package: {info.android_package})"
            )
        gotchas.append("Never commit keystore files or google-services.json")
        gotchas.append("local.properties contains machine-specific SDK paths")
        conventions = ["Follow Android Kotlin style guide", "Prefer coroutines for async"]
        if info.android_min_sdk:
            conventions.append(f"Min SDK: {info.android_min_sdk}")

    elif pt == "ios":
        architecture = (
            "iOS application. "
            "Standard Xcode project structure."
        )
        if info.ios_bundle_id:
            structure["*.xcodeproj"] = f"Xcode project (bundle: {info.ios_bundle_id})"
        gotchas.append("Never commit .p12 certificates or .mobileprovision files")
        gotchas.append("GoogleService-Info.plist contains sensitive Firebase config")
        conventions = ["Follow Swift API Design Guidelines", "Prefer async/await for concurrency"]

    elif pt == "flutter":
        architecture = "Flutter cross-platform application."
        structure["lib/"] = "Dart application code"
        structure["pubspec.yaml"] = "Dependencies and project metadata"
        if info.flutter_sdk:
            conventions.append(f"Flutter SDK: >={info.flutter_sdk}")
        gotchas.append("Run `flutter pub get` after changing pubspec.yaml")

    elif pt == "react_native":
        architecture = "React Native cross-platform application."
        structure["src/"] = "Application source code"
        gotchas.append("Never commit .env files with API keys")

    if info.has_ci:
        conventions.append("CI is configured – ensure tests pass before merging")

    return WorkspaceModel(
        project_name=info.project_name,
        tech_stack=tech_stack,
        architecture=architecture,
        conventions=conventions,
        structure=structure,
        gotchas=gotchas,
    )


